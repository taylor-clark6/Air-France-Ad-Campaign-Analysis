# Select relevant columns
cols = ['Engine Click Thru %', 'Click Charges', 'Avg. Cost per Click',
        'Probability of Booking', 'Avg. Pos.']

# Get the correlation
corr = df[cols].corr()

# Plot correlation heatmap
plt.figure(figsize=(8,6))
sns.heatmap(corr, annot=True, cmap='coolwarm', fmt=".2f")
plt.title('Correlation Between Metrics and Average Position', fontsize=14, fontweight='bold')
plt.show()

# Select numeric columns
numeric_df = df.select_dtypes(include='number')

# Compute correlations with Avg. Pos.
corr_with_avgpos = numeric_df.corr()['Avg. Pos.'].sort_values(ascending=False)

# Display as a horizontal bar chart
plt.figure(figsize=(8,6))
corr_with_avgpos.drop('Avg. Pos.').plot(kind='barh', color='steelblue')
plt.title('Correlation of Metrics with Average Position', fontsize=14, fontweight='bold')
