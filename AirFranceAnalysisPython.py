# Summarize average rates by publisher
publisher_rates = df.groupby('Publisher Name').agg({
    'Trans. Conv. %': 'mean',
    'Engine Click Thru %': 'mean'
}).reset_index()

# Melt data for easy grouped plotting
rates_melted = publisher_rates.melt(id_vars='Publisher Name',
                                    value_vars=['Trans. Conv. %', 'Engine Click Thru %'],
                                    var_name='Metric',
                                    value_name='Percentage')

# Plot the data
plt.figure(figsize=(10,6))
sns.barplot(data=rates_melted, x='Publisher Name', y='Percentage', hue='Metric')

