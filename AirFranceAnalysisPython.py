"""<h1>Question 1</h1>"""

# Summarize the data by publisher
publisher_summary = df.groupby('Publisher Name').agg({
    'Trans. Conv. %': 'mean',
    'Net Revenue': 'sum',
    'Return on Ad Dollar Spent': 'mean'
}).reset_index()

# Normalize metrics for fair comparison, scaling each metric between 0 and 1
normalized = publisher_summary.copy()
normalized[['Trans. Conv. %', 'Net Revenue', 'Return on Ad Dollar Spent']] = (
    publisher_summary[['Trans. Conv. %', 'Net Revenue', 'Return on Ad Dollar Spent']]
    .apply(lambda x: (x - x.min()) / (x.max() - x.min()))
)

# Plot grouped bar chart
chart = normalized.set_index('Publisher Name').plot(
    kind='bar',
    figsize=(12,6),
    width=0.75,
    color=['#4E79A7', '#F28E2B', '#76B7B2']
)

